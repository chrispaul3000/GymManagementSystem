        // TODO add your handling code here:
        String admin_id = adminIDLabel.getText();
        String username =jTextField1.getText();
        String password =String.valueOf(jPasswordField1.getPassword());
        String role = jTextField2.getText();
        
        String updateAdminQuery = "UPDATE tbl_member SET mem_lname = ?, mem_fname =? WHERE mem_id = ?";
        try{
            String updateMemberQuery ="UPDATE tbl_admin SET username = ?, password =? WHERE role = ?";
            ps = MyConnection.getConnection().prepareStatement(updateMemberQuery);
            ps.setString(1, username);
            ps.setString(2, password);
            ps.setString(3, role);
        
            ps.executeUpdate();
            dashboard.populateAdminTable();
            this.dispose();
        }catch(Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage());
}
